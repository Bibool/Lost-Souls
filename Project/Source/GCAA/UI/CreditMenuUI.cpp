#include "CreditMenuUI.h"
#include "OptionsMenuUI.h"
#include "Kismet/GameplayStatics.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////
void  UCreditMenuUI::NativeConstruct ()
{
	Super::NativeConstruct ();
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////
void UCreditMenuUI::NativeTick ( const FGeometry& MyGeometry, float InDeltaTime )
{
	Super::NativeTick ( MyGeometry, InDeltaTime );

	if (!UGameplayStatics::IsGamePaused ( GetWorld () ))
	{
		this->RemoveFromParent ();
	}
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////
void UCreditMenuUI::ReturnButtonClicked ()
{
	if (pReturnPtr)
	{
		pReturnPtr->RestoreVisibility ();
	}
	this->RemoveFromParent ();
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////
void  UCreditMenuUI::AddReturnPointer ( UOptionsMenuUI* ptrToReturnMenu )
{
	pReturnPtr = ptrToReturnMenu;
}




